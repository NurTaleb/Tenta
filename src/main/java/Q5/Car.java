package Q5;

public class Car {
    /**
     * This constructs a cars with a specified color, brand and numberOfCras
     */
    private String color;
    private String brand;
    private String numberOfCars;

    /**
     * Uses getter and setters
     * This gets you the color,brand and numberOfCars
     * @return
     */

    public Car ( String color, String brand, String numberOfCars) {


    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getNumberOfCars() {
        return numberOfCars;
    }

    public void setNumberOfCars(String numberOfCars) {
        this.numberOfCars = numberOfCars;
    }
}
